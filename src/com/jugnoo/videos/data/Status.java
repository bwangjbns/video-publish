package com.jugnoo.videos.data;

/**
 * 创建视频过程中出现的几种状态
 * 
 * @author bwang
 * 
 */
public enum Status {

	WAIT("waiting"),

	PROCESS("processing"),

	FINISH("finished"),

	FAIL("failed"),

	LOST("lost");

	private String s;

	private Status(String s) {
		this.s = s;
	}

	public String getStatus() {
		return this.s;
	}

}
